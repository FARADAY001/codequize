  /// Nom affichable d'un langage à partir de son id, ou l'id lui-même si
  /// inconnu 
  static String nomLangage(String langageId) {
    final trouve = langages.where((l) => l.id == langageId);
    return trouve.isEmpty ? langageId : trouve.first.nom;
  }
